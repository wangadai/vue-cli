Location.prototype = {
	init: function(backfill){
		var _this = this, domsId = _this.ops.domsId;

		//初始化select2
		$("#" + domsId.province + ",#" + domsId.city + ",#" + domsId.district).select2();

		//默认初始化省份列表
		if (backfill && $("input[name="+domsId.district+"]").val())
			_this.setValue(backfill);
		else
			_this._render(0, "province");

		_this._eventInit();
	},
	getValue: function(){
		var domsId = this.ops.domsId,
        province = $("#"+domsId.province).select2("data")[0].text,
        city = $("#"+domsId.city).select2("data")[0].text,
        district = $("#"+domsId.district).select2("data")[0].text;

		return {
			province: province ==='省份'?'':province,
			city: city==='地级市'?'':city,
			district: district==='市、县、区'?'':district,
		}
	},
	setValue: function(ops){
		var domsId = this.ops.domsId;
	    var province=$('#'+domsId.province+' option').filter(function(i,v){
	      return v.innerText==ops.province;
	    });
	    if(province.length>0){
	      $('#'+domsId.province).val(province[0].value).trigger('change');
	      var city=$('#'+domsId.city+' option').filter(function(i,v){
	        return v.innerText==ops.city;
	      });
	      if(city.length>0){
	        $('#'+domsId.city).val(city[0].value).trigger('change');
	        var district=$('#'+domsId.district+' option').filter(function(i,v){
	          return v.innerText==ops.district;
	        });
	        if(district.length>0){
	          $('#'+domsId.district).val(district[0].value).trigger('change');
	        }
	      }
	    }
	},
	_reset: function(type){	//param String province or city or district
		var domsId = this.ops.domsId, title = this.ops.title;

		for (var key in domsId){
			if (type == "city" && key == "province") continue;
			if (type == "district" && (key == "province" || key == "city")) continue;

			$("#s2id_" + domsId[key]).find(".select2-chosen").text(title[key]);
			$("#" + domsId[key]).html("<option value=''>"+ title[key] +"</option>");
			var name = key == "district" ? "district" : key;
			!this.ops.backfill&&$('input[name="' + name + '"]').val("");
		}
	},
	_render: function(id, type){	//param String province or city or district
		this._reset(type);
		var html = "<option value=''>"+ this.ops.title[type] +"</option>",
		 	domId = this.ops.domsId[type];

		html += this._getHtml(id);

		$("#" + domId).html(html);
	},
	_getHtml: function(loc_id){
		var json = this.items[loc_id] || "", html = "";
		if (json) {
			$.each(json , function(k , v) {
				html += '<option value="'+k+'">'+v+'</option>';
			})
		}else{
			var idArr = loc_id.split(",");
			var districtId = idArr[2];
			idArr.length=2;
			var cityId = idArr.join(",");
			var districtName = this.items[cityId][districtId];
			html += '<option value="'+districtId+'">'+districtName+'</option>';
		}
		return html;
	},
	_eventInit: function(){
		var _this = this, domsId = this.ops.domsId;
		$("#" + domsId.province).change(function() {
			if($(this).val()){
		        _this._render('0,' + $(this).val(), "city");
		        $('input[name='+domsId.province+']').val($(this).find("option:selected").text());
		    }else
		        _this._render(0, "province");
		});
		$("#" + domsId.city).change(function() {
			if($(this).val()){
				_this._render('0,' + $('#' + domsId.province).val() + ',' + $(this).val(), "district");
		        $('input[name='+domsId.city+']').val($(this).find("option:selected").text());
		    } else
		    	_this._render('0,' + $('#' + domsId.province).val(), "city");
		});
		$("#" + domsId.district).change(function() {
			if($(this).val()){
		        $('input[name='+domsId.district+']').val($(this).find("option:selected").text());
		    } else
		    	_this._render('0,' + $('#' + domsId.province).val() + ',' + $('#' + domsId.city).val(), "district");
		});
	},
	refind: function(addr) {
		if(!addr) return "";
		var arr = addr.split(" ");
		var pro = arr[0],city = arr[1],district = arr[2];
		var result=[];
		var items = this.items;
		for(var k in items["0"]){
			if(items["0"][k] == pro){
				result[0] = k;
			}
		}
		for(var k in items["0,"+result[0]]){
			if(items["0,"+result[0]][k] == city){
				result[1] = k;
			}
		}
		for(var k in items["0,"+result[0]+","+result[1]]){
			if(items["0,"+result[0]+","+result[1]][k] == district){
				result[2] = k;
			}
		}
		return result;
	}
}

